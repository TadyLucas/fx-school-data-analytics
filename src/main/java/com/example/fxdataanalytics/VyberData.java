package com.example.fxdataanalytics;

public class VyberData {
    private final int id;
    private final int vyber;

    public VyberData(int id, int vyber) {
        this.id = id;
        this.vyber = vyber;
    }

    public int getId() {
        return id;
    }

    public int getVyber() {
        return vyber;
    }
}

