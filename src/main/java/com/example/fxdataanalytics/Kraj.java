package com.example.fxdataanalytics;

public class Kraj {
    private int id;
    private String nazev;

    public Kraj(int id, String nazev) {
        this.id = id;
        this.nazev = nazev;
    }
    public int getId() {
        return id;
    }
    public String getNazev() {
        return nazev;
    }

}
